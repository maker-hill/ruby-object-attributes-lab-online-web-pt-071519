class Dog 
  
  def initialize(name)
    @name = name
  end
  
  def initialize(breed)
    @breed = breed
  end
  
  def name=(dog_name)
    
    dog_name = @name
  end
  
  def breed=(dog_breed)
    
    @breed = dog_breed
  end
  
  def name
    @name
  end
  
  def breed
    @breed
  end  
  
end   